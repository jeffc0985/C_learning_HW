#include <stdio.h>

int main()
{
    //像素資料 
    int pixel[3][4][5] = 
        {{{247,67,32,187,240},{122,41,21,16,154},{52,35,79,21,93},{27,22,35,154,75}},
        {{14,145,132,25,40},{212,221,121,54,14},{132,235,178,19,14},{122,122,133,54,47}},
        {{17,44,32,127,240},{22,231,21,156,124},{32,35,78,21,194},{127,22,33,54,45}}};
    
    int i, j, k;

    //R區+30,超過255以255計
    i = 0;
    for(j=0;j<4;j++)
        for(k=0;k<5;k++)
        {
            pixel[i][j][k] += 30;

            if(pixel[i][j][k]>255)
                pixel[i][j][k] = 255;
        }

    //G區-30,不足0以0計
    i = 1;
    for(j=0;j<4;j++)
        for(k=0;k<5;k++)
        {
            pixel[i][j][k] -= 30;
            
            if(pixel[i][j][k]<0)
                pixel[i][j][k] = 0;
        }
    
    //print出
    for(i=0;i<3;i++)
    {
        for(j=0;j<4;j++)
        {   
            for(k=0;k<5;k++)
                printf("%3d ",pixel[i][j][k]);
            printf("\n");
        }

        printf("*********************\n");
    }

    
    return 0;
}